    private void LoadEmployeeId() {

        try
        {
            empList=EmployeeDAO.getAllEmployeeId();
            for(String e:empList.keySet())
                jcEmpId.addItem(e);
        }
                catch(SQLException sq)
                    {
                    JOptionPane.showMessageDialog(null,"Problem in Database!","Error!",JOptionPane.ERROR_MESSAGE);
                    sq.printStackTrace();
                    }
        
            }

