    private void jcEmpIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcEmpIdActionPerformed

        if(jcEmpId.getSelectedItem()==null)
        {
            return;
        }
        try{
        String str=jcEmpId.getSelectedItem().toString();
        int eno=Integer.parseInt(str);
         Employee e=EmployeeDAO.findEmployeeById(eno);
            if(e==null)
                JOptionPane.showMessageDialog(null,"No Records Of EmpNo "+eno+"Present","not Found",JOptionPane.INFORMATION_MESSAGE);
            else
            {
                txtEmpName.setText(e.getEname());
                txtEmpJob.setText(e.getJob());
                txtEmpSal.setText(String.valueOf(e.getSal()));
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"Error In DataBase!","Exception!",JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    
    }//GEN-LAST:event_jcEmpIdActionPerformed

