    private void loadCategoryId() {
      
    try
    {
        categories=CategoryDao.getCategoryId();
        for(String catId:categories.keySet())
            jcCatId.addItem(catId);
        
    }
    catch(SQLException sq)
    {
        JOptionPane.showMessageDialog(null,"Problem In dataBase!","Error!",JOptionPane.ERROR_MESSAGE);
        sq.printStackTrace();
    }
    }


