    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
      
       if(txtCatName.getText().isEmpty())
       {
           JOptionPane.showMessageDialog(null,"please provide Category Name !","Empty Field!",JOptionPane.ERROR_MESSAGE);
           return;
       }
      try
      {
           Category cat=new Category();
            cat.setCatId(jcCatId.getSelectedItem().toString());
            cat.setCatName(catName);
            cat.setCatName(txtCatName.getText());
        
          boolean rs=CategoryDao.updatCategory(cat);
          if(rs)
        {
            JOptionPane.showMessageDialog(null,"Category Updation Successfully","success",JOptionPane.INFORMATION_MESSAGE);
        }
        else
        {
           JOptionPane.showMessageDialog(null,"Updation Failed","Failed!!",JOptionPane.ERROR_MESSAGE);

        }
      }
      catch(Exception e)
      {
           JOptionPane.showMessageDialog(null,"Error In DataBase!","error!",JOptionPane.INFORMATION_MESSAGE);
  
      }
        
    }//GEN-LAST:event_btnAddActionPerformed

    private void jcCatIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcCatIdActionPerformed

        if(jcCatId.getSelectedItem()==null)
            return;
        String catId=jcCatId.getSelectedItem().toString();
        String c=categories.get(catId);
        txtCatName.setText(c);
        
    }//GEN-LAST:event_jcCatIdActionPerformed

