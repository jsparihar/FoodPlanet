    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed

        try
        {
            
        employeeList=EmployeeDAO.getAllEmployee();
        if(employeeList.isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Invalid UserId !","wrong !",JOptionPane.ERROR_MESSAGE );
            return;
        }
        else
        {
            DefaultTableModel model=(DefaultTableModel)jtEmployee.getModel();
            Object row[]=new Object[4];  // 4no. of column 
            int x=model.getRowCount();
            for(int i=x-1; i>=0;i--)
                model.removeRow(i);
            for(Employee e:employeeList)
            {
                row[0]=e.getEmpid();
                row[1]=e.getEname();
                row[2]=e.getJob();
                row[3]=e.getSal();
                model.addRow(row);
               
            }
        }
    }
    catch(SQLException sq)
               {
               JOptionPane.showMessageDialog(null,"Error while Adding Product !","Exception !",JOptionPane.ERROR_MESSAGE);
               sq.printStackTrace();
               }
        
    }//GEN-LAST:event_btnSearchActionPerformed

